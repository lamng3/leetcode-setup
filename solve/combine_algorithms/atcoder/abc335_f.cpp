void solve() {
    int N; cin >> N;
    vi A(N);
    for (int i = 0; i < N; i++) cin >> A[i];

    int sqrtN = sqrt(N);

    // dp[i] = # sets of squares
    vi dp(N, 1);

    // M[step size][last possible position] = prefix DP sum counting # sets of squares
    vii M(sqrtN, vi(sqrtN, 0));

    for (int i = N-1; i >= 0; i--) {
        if (A[i] >= sqrtN) {
            for (int j = i+A[i]; j < N; j+=A[i]) {
                dp[i] += dp[j];
                dp[i] %= MOD_NTT;
            }
        }
        else {
            dp[i] += M[A[i]][i%A[i]];
            dp[i] %= MOD_NTT;
        }
        for (int j = 1; j < sqrtN; j++) {
            M[j][i%j] += dp[i];
            M[j][i%j] %= MOD_NTT;
        }
    }

    cout << dp[0] << '\n';
}

